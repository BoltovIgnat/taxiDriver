const defaultBadges = [
  "no-deposit",
  "car-provided",
  "own-car",
  "daily-payouts",
  "license-help",
  "direct-connection",
] as const;

function city(
  slug: string,
  name: string,
  prep: string,
  region: string,
  rubPerHour: number,
  rentalCost: number,
  displayRange: string,
  featured: boolean,
  highlights: string[],
): City {
  return {
    slug,
    name,
    namePrepositional: prep,
    region,
    isFeatured: featured,
    metaTitle: `Работа в такси в ${prep} — ${displayRange}`,
    metaDescription: `Подключение водителей такси в ${prep} — ${displayRange}. Выплаты каждый день, авто без залога или на своём. Оставьте заявку — перезвоним за 15 минут.`,
    income: {
      ownCar: { rubPerHour, commissionPercent: 15 },
      rental: {
        rubPerHour: rubPerHour - 30,
        rentalCostPerDay: rentalCost,
        commissionPercent: 15,
      },
      displayRange,
    },
    conditions: {
      badges: [...defaultBadges],
      highlights,
      rentalAvailable: true,
      ownCarAllowed: true,
      dailyPayouts: true,
      noDeposit: true,
    },
    heroSubtitle: `Стабильный доход ${displayRange}. Подключение за 1 день, выплаты на карту каждый вечер.`,
    description: `Работа водителем такси в ${prep} — один из самых гибких способов заработка в городе. Мы подключаем к агрегаторам напрямую, без посредников и скрытых комиссий. Можно работать на своём автомобиле или взять авто в аренду без залога. График свободный: вы сами выбираете, сколько часов выходить на линию. Помогаем оформить самозанятость, лицензию такси и все документы. Выплаты на карту ежедневно, поддержка 24/7. Средний доход активных водителей в ${prep} — ${displayRange} при выходе 6–7 дней в неделю.`,
    photo: getCityPhoto(slug, name),
    faqIds: ["docs", "self-employed", "rental-vs-own", "payouts", "schedule"],
    reviewIds: ["1", "2", "3"],
  };
}

